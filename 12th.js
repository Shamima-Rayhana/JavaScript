//Iterate with JavaScript While Loops
var ourArray = [];
var i = 5;
while(i >=0) {
  ourArray.push(i);
  i--;
}
//Iterate with JavaScript For Loops
// Setup
var myArray = [];

for(var i=1;i<=5;i++)
{
  myArray.push(i);
}
//Iterate Odd Numbers With a For Loop
// Setup
var myArray = [];

for(var i=1;i<=9;i+=2)
{
  myArray.push(i);
}
//Count Backwards With a For Loop
// Setup
var myArray = [];

for(var i=9;i>=1;i-=2)
{
  myArray.push(i);
}